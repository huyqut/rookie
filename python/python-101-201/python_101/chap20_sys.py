# "sys" module

import sys


print(sys.argv)  # Arguments passed to this python program
print(sys.executable)  # Path to Python interpreter
print(sys.copyright)  # Copyright info of Python interpreter
print(sys.version)  # Version of this interpreter
print(sys.version_info)  # Info of version
print(sys.api_version)  # API version
print(sys.path)  # Search path for modules
print(sys.platform)  # Platform in which the program is run
print(sys.stdin)
print(sys.stdout)
print(sys.stderr)

sys.exit(0)  # Exit program with status
